<?php

namespace App\Exports\Sales;

use Maatwebsite\Excel\Concerns\FromCollection;
use Maatwebsite\Excel\Concerns\WithHeadings;
use Maatwebsite\Excel\Concerns\WithMapping;
use Maatwebsite\Excel\Concerns\ShouldAutoSize;
use Maatwebsite\Excel\Concerns\WithStyles;
use PhpOffice\PhpSpreadsheet\Worksheet\Worksheet;
use Illuminate\Support\Collection;

class ShipmentExport implements FromCollection, WithHeadings, WithMapping, ShouldAutoSize, WithStyles
{
    public function __construct(
        protected Collection $collection
    ) {}

    public function collection()
    {
        $footer = (object) [
            'is_footer' => true,
            'message' => 'Generated by System on ' . now()->format('d M Y, H:i:s')
        ];
        
        return $this->collection->push($footer);
    }

    public function headings(): array
    {
        return [
            'SHIPMENT NUM',
            'DATE',
            'CUSTOMER NAME',
            'DO NUM',
            'SO NUM',
            'WAREHOUSE NAME',
        ];
    }

    public function map($row): array
    {
        if (isset($row->is_footer)) {
            return [$row->message];
        }

        return [
            $row->shipment_number,
            $row->date,
            $row->customer_name,
            $row->delivery_order_number ?? '-',
            $row->sales_order_number ?? '-',
            $row->warehouse_name ?? '-',
        ];
    }

    public function styles(Worksheet $sheet)
    {
        return [
            1 => ['font' => ['bold' => true]],
        ];
    }
}
